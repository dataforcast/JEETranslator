package edu.subtitletranslator.beans;
	/*-----------------------------------------------------------------------*/
	/**
	 * This class handles all statisitics related to translation process.
	 * Those informations aims to be viewed on HTML pages. 
	 * @author bangui
	 *
	 */
	/*-----------------------------------------------------------------------*/
	public class SubtitleStatsBean {
		private Integer originalLines=0;
		private Integer translatedLines=0;
		private String  fileName = null;
		private String  jdbcEngine=null;
	/*-----------------------------------------------------------------------*/
	
	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @param fileName : subtitle file name.
	 */
	/*-----------------------------------------------------------------------*/
	public SubtitleStatsBean(String fileName ){
		this.fileName = fileName;
	}
	/*-----------------------------------------------------------------------*/
	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return fileName
	 */
	/*-----------------------------------------------------------------------*/

	public String getFileName() {
		return fileName;
	}
	/*-----------------------------------------------------------------------*/

	
	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public Integer getOriginalLines() {
		return originalLines;
	}
	/*-----------------------------------------------------------------------*/

	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void addOriginalLines(Integer originalLines) {
		this.originalLines += originalLines;
	}
	/*-----------------------------------------------------------------------*/

	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public Integer getTranslatedLines() {
		return translatedLines;
	}
	/*-----------------------------------------------------------------------*/

	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void setTranslatedLines(Integer translatedLines) {
		this.translatedLines = translatedLines;
	}
	/*-----------------------------------------------------------------------*/
	/*-----------------------------------------------------------------------*/
	/**
	 * 
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void setOriginalLines(Integer originalLines2) {
		this.originalLines = originalLines2;		
	}
	/*-----------------------------------------------------------------------*/
	/*-----------------------------------------------------------------------*/
	/**
	 * Increase translated lines with 1 unit.
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void increaseTranslatedLine() {
		this.translatedLines++;
	}
	/*-----------------------------------------------------------------------*/

	/*-----------------------------------------------------------------------*/
	/**
	 * Decrease translated lines with 1 unit.
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void decreaseTranslatedLine() {
		this.translatedLines--;
	}
	/*-----------------------------------------------------------------------*/
	/*-----------------------------------------------------------------------*/
	/**
	 * Decrease translated lines with 1 unit.
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public String getJdbcEngine() {
		return jdbcEngine;
	}
	/*-----------------------------------------------------------------------*/
	/**
	 * Decrease translated lines with 1 unit.
	 * @return
	 */
	/*-----------------------------------------------------------------------*/
	public void setJdbcEngine(String jdbcEngine) {
		this.jdbcEngine = jdbcEngine;
	}
	/*-----------------------------------------------------------------------*/
	
}
